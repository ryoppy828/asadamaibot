export function getRubberContext(): string {
  if (rubberDatabase.length === 0) return "";
  let context = "--- 卓球ラバーデータベース ---\n\n";
  rubberDatabase.forEach(r => {
    context += `【${r.name}】(${r.maker} / ${r.type})\n  特徴: ${r.feature}\n  対象: ${r.target}\n`;
    if (r.hardness) context += `  硬度: ${r.hardness}\n`;
    if (r.speed) context += `  スピード: ${r.speed}\n`;
    if (r.spin) context += `  スピン: ${r.spin}\n`;
    context += "\n";
  });
  return context;
}
